<script>
    function getstock() {
        var firststock = $('#firststock').val();
        var product = '<?= $product->id ?>';

        if (firststock != 0) {
            $.ajax({
                url: '<?= base_url('home/getstock') ?>',
                type: 'POST',
                data: {'product': product, 'firststock': firststock},
                dataType: 'json',
                success: function (data) {
                    $('#secondstock').empty();
                    $('#stockinfo').empty();
                    $.each(data, function (i, stock) {
                        $('#secondstock').append($('<option>', {
                                value: stock.id,
                                text: stock.name
                            }
                        ));
                        if (i == 0 && stock.stock < 6) {
                            $('#stockinfo').html('Bu seçenekler için son ' + stock.stock + ' ürün!');
                        }
                    });
                }
            })
        } else {
            $('#secondstock').empty();
            $('#secondstock').append(
                $('<option>', {text: 'Önce <?= Secenekler::find($stocktype->option1)->name ?> seçimi yapınız.'})
            );
        }
    }

    function getcountstock(id) {
        var firststock = $('#firststock').val();
        var secondstock = $('#secondstock').val();
        var product = id;

        $.ajax({
            url: '<?= base_url('home/getcountstock') ?>',
            type: 'POST',
            data: {'product': product, 'firststock': firststock, 'secondstock': secondstock},
            success: function (data) {
                $('#stockinfo').empty();
                if (data < 6) {
                    $('#stockinfo').html('Bu seçenekler için son ' + data + ' ürün!');
                }
            }
        })
    }
</script>
